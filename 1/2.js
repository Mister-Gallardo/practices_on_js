// Объявите переменные, имеющие различные типы данных. Выведите в консоль
// их значения и типы данных, используя оператор typeof.

// Объявление переменных различных типов данных
let number = 42;          // Number
let string = "Hello";     // String
let isTrue = true;       // Boolean
let obj = { key: "value" }; // Object
let arr = [1, 2, 3];     // Array
let nothing = null;       // Null
let notDefined;           // Undefined

// Вывод значений и их типов данных в консоль
console.log(number, typeof number);
console.log(string, typeof string);
console.log(isTrue, typeof isTrue);
console.log(obj, typeof obj);
console.log(arr, typeof arr);
console.log(nothing, typeof nothing);
console.log(notDefined, typeof notDefined);
