// Напишите программу, которая выводит числа от 1 до 10 с помощью различных
// циклов: while, do…while, for

// Цикл while
console.log("Цикл while:");
let i = 1;
while (i <= 10) {
    console.log(i);
    i++;
}

// Цикл do...while
console.log("Цикл do...while:");
let j = 1;
do {
    console.log(j);
    j++;
} while (j <= 10);

// Цикл for
console.log("Цикл for:");
for (let k = 1; k <= 10; k++) {
    console.log(k);
}
