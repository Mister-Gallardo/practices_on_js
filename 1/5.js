// Создайте функцию, которая принимает два аргумента, выполняет над ними
// математическую операцию и возвращает ее результат. Используйте три
// различных синтаксиса описания функции.

// 1. Функция-объявление
function add(a, b) {
    return a + b;
}

// 2. Функция-выражение
const subtract = function(a, b) {
    return a - b;
};

// 3. Стрелочная функция
const multiply = (a, b) => a * b;

// Примеры использования функций
console.log("Сумма:", add(5, 3));
console.log("Разность:", subtract(5, 3));
console.log("Произведение:", multiply(5, 3));
