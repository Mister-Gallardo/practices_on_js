// Создайте набор имен с помощью Set, добавьте в него четыре значения. Удалите
// первый добавленный элемент, проверьте его отсутствие в наборе, выведите в
// консоль его размер

// Создание набора имен с помощью Set
let nameSet = new Set();

// Добавление четырех значений
nameSet.add("Alice");
nameSet.add("Bob");
nameSet.add("Charlie");
nameSet.add("David");

// Удаление первого добавленного элемента
nameSet.delete("Alice");

// Проверка отсутствия элемента в наборе
console.log("Содержит 'Alice':", nameSet.has("Alice"));

// Вывод размера набора
console.log("Размер набора:", nameSet.size);
