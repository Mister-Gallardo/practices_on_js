// возвращает массив имен. Создайте функцию нахождения среднего
// арифметического для массива чисел.

// Создание массива с пятью произвольными элементами
let myArray = [10, "hello", true, null, 42];

// Вывод первого и последнего элементов массива
console.log("Первый элемент:", myArray[0]);
console.log("Последний элемент:", myArray[myArray.length - 1]);

// Обновление второго элемента массива
myArray[1] = "world";

// Создание второго массива как копии первого
let copiedArray = [...myArray];

// Вывод обоих массивов в консоль
console.log("Обновленный массив:", myArray);
console.log("Копия массива:", copiedArray);

// Функция, которая объединяет два массива в один
function mergeArrays(arr1, arr2) {
    return [...arr1, ...arr2];
}

// Пример использования функции объединения массивов
let mergedArray = mergeArrays(myArray, copiedArray);
console.log("Объединенный массив:", mergedArray);

// Массив пользователей
let users = [
    { name: "Alice" },
    { name: "Bob" },
    { name: "Charlie" }
];

// Функция, которая возвращает массив имен пользователей
function getUserNames(usersArray) {
    return usersArray.map(user => user.name);
}

// Пример использования функции
let userNames = getUserNames(users);
console.log("Имена пользователей:", userNames);

// Функция нахождения среднего арифметического для массива чисел
function calculateAverage(numbers) {
    let sum = numbers.reduce((acc, num) => acc + num, 0);
    return sum / numbers.length;
}

// Пример использования функции среднего арифметического
let numbersArray = [10, 20, 30, 40, 50];
let average = calculateAverage(numbersArray);
console.log("Среднее арифметическое:", average);
