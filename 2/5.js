// элемента, выведите в консоль значение последнего добавленного элемента.
// Удалите любой элемент, проверьте его отсутствие в коллекции.

// Создание коллекции ключ-значение с помощью Map
let myMap = new Map();

// Добавление трех элементов
myMap.set("name", "Alice");
myMap.set("age", 25);
myMap.set("city", "New York");

// Вывод значения последнего добавленного элемента
console.log("Значение последнего добавленного элемента (city):", myMap.get('city'));

// Удаление любого элемента
myMap.delete("age");

// Проверка отсутствия элемента в коллекции
console.log("Содержит 'age':", myMap.has("age"));
