// Создайте объект, содержащий информацию о пользователе (имя, возраст,
// город), выведите его в консоль. Измените значение возраста, добавьте в объект
// новое свойство, а одно из существующих удалите, выведите обновленные
// данные. Создайте объект второго пользователя и посчитайте сумму возрастов
// двух пользователей.

// Создание объекта пользователя
let user1 = {
    name: "Alice",
    age: 25,
    city: "New York"
};

// Вывод объекта в консоль
console.log("Первый пользователь:", user1);

// Изменение возраста
user1.age = 26;

// Добавление нового свойства
user1.country = "USA";

// Удаление существующего свойства
delete user1.city;

// Вывод обновленных данных
console.log("Обновленный первый пользователь:", user1);

// Создание второго пользователя
let user2 = {
    name: "Bob",
    age: 30,
    city: "Los Angeles"
};

// Сумма возрастов двух пользователей
let totalAge = user1.age + user2.age;
console.log("Сумма возрастов двух пользователей:", totalAge);
